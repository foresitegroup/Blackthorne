<h1 style="margin-bottom: 0.8em;">Investment Strategy</h1>

Blackthorne Partners typically acquires, or invests in, companies that:<br>
<ul>
  <li>Generate a minimum of $1 million EBITDA</li>
  <li>Have revenues of $5 million to $20 million</li>
  <li>Have ownership transitioning out of the business, or are in need of a cash infusion to make a leap forward</li>
  <li>Specialize in essential products or services, making the businesses less vulnerable to economic volatility</li>
  <li>Have a quality reputation in a stable industry</li>
  <li>Can grow organically and/or through add-on acquisition</li>
  <li>Are based in or near Southeast Wisconsin</li>
</ul>
<br>

<strong>Ownership and management</strong><br>
<ul>
  <li>Blackthorne Partners believes that small businesses provide attractive investment opportunities. We can create value by building these companies in partnership with management.</li>
  <li>When required, we provide the leadership that is sometimes needed at smaller firms in a state of transition.</li>
</ul>
<br>

<strong>Primary businesses of interest</strong><br>
<ul>
  <li><strong>Outsourced business services.</strong> Blackthorne Partners has a proven history of business building in this category.</li>
  <li><strong>Light manufacturing.</strong> These companies typically fit our criteria of having a quality reputation in a stable industry. We also look for manufacturers with limited economic vulnerability.</li>
  <li><strong>Value-added distribution.</strong> Our business service successes can be leveraged in the distribution sector as well.</li>
</ul>

