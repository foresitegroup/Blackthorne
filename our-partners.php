<h1>Our Partners</h1>
<h2>The right partners</h2>

The word "Partners" is in our name for a reason.<br>
<br>

Our approach at Blackthorne Partners is to work with small, niche businesses seeking a partner to see them through a time of transition. We provide the private equity investment and expertise for a smooth transition, and future growth.<br>
<br>

Other small businesses might be ready to make a crucial step forward. Blackthorne can provide the necessary capital, and also leverage our experience to achieve top-line and bottom-line growth targets.<br>
<br>

We partner with investors, too. They, along with principals of Blackthorne Partners and management, provide the necessary capital.<br>
<br>

<strong>Businesses</strong><br>
Blackthorne Partners most frequently invests in small businesses (EBIDTA of about $1 million). Among those businesses are:<br>
<ul>
  <li>Family-owned companies looking for exit opportunities, and an assurance that their legacy will be maintained.</li>
  <li>Successful operations where the leader seeks additional professional management, and perhaps a reduced role.</li>
  <li>Firms that need a partner who can supply the capital and strategy necessary to grow.</li>
</ul>
<br>

<strong>Investors</strong><br>
Crucial to Blackthorne Partners are investors who make the acquisitions and partnerships possible. Our investors:<br>
<ul>
  <li>Are professionals who understand the value of investing in small businesses; many are former or current business owners.</li>
  <li>Are able to pick and choose which businesses they invest in, compared with putting dollars in a private equity fund.</li>
  <li>Desire a more intimate, direct investment than they would receive from most other private equity firms.</li>
</ul>
<br>

<strong>Leadership/Managers</strong><br>
Some Blackthorne investments require an immediate leadership infusion.<br>
<ul>
  <li>We maintain contact with skilled managers to ensure we can provide replacement or supplemental management expertise when needed.</li>
  <li>On occasion, we partner with managers in pursuit of a particular business or businesses.</li>
</ul>

