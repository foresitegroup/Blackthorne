John, a Chartered Financial Analyst (CFA), has a 30-year history of helping public, private and institutional investors reach their financial goals.<br>
<br>

A graduate of the University of Wisconsin-Madison with a B.B.A. in Finance, John began his investment career at The Marine Trust Company (now JPMorgan Chase), where he worked as an equity analyst and portfolio manager for owners of high-net-worth, personal trust accounts.<br>
<br>

Since then, he has held a variety of positions involving both individual and institutional investment management. That experience includes time in management and capital markets with Blunt Ellis and Loewi, and as a shareholder in Cleary Gull, a Milwaukee-based investment banking and brokerage firm focused on the institutional marketplace.<br>
<br>

John founded Blackthorne Partners in fall 2006 to help provide high-net-worth individuals with easier access to private equity investments.<br>
<br>

John has a history of professional and personal engagement among his peers and in the community. He is a member of the Association for Corporate Growth and the CFA Society of Milwaukee. John has always been active in the community and currently serves on the regional advisory board of Young Life.<br>
